/**
  * @brief  The firmware entry point.
  *
  * main.c is intentionally kept clean: board setup is grouped in the BSP layer,
  * and runtime behavior is grouped in the App layer. This keeps startup order
  * visible here without mixing hardware details into the main loop.
  * @retval int
  */
int main(void)
{
    /* Initialize HAL, clocks, GPIO, and UART peripherals before app code runs. */
    BSP_System_Init();

    /* Initialize application modules after the board support package is ready. */
    App_Init();

    while (1)
    {
        /* Run non-blocking application tasks continuously. */
        App_Loop();
    }
}

